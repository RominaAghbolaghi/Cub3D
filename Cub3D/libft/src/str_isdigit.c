#include "libft.h"

/* ************************************************************************** */

bool	str_isdigit(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (ft_isspace(str[i]) || ft_isdigit(str[i]) || str[i] == 'N'
			|| str[i] == 'S' || str[i] == 'E' || str[i] == 'W')
			i++;
		else
			return (false);
	}
	return (true);
}

